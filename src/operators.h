#ifndef OPERATORS
#define OPERATORS

unsigned float addition (unsigned float num1, unsigned float num2) {
	return (num1 + num2);
}

unsigned float subtraction (unsigned float num1, unsigned float num2) {
	return (num1 - num2);
}

unsigned float multiplication (unsigned float num1, unsigned float num2) {
	return (num1 * num2);
}

unsigned float division (unsigned float num1, unsigned float num2) {
	return (num1 / num2);
}

#endif
